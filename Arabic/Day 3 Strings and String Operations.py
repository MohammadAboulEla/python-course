# String Operations
greeting = "Hello, World!"
print(greeting)

# String Concatenation
first_name = "John"
last_name = "Doe"
full_name = first_name + " " + last_name
print(full_name)

# String Length and Indexing
print(len(greeting))
print(greeting[0])  # First character
print(greeting[-1])  # Last character
